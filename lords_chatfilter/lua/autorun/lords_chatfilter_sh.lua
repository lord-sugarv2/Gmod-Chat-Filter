function LordsFilter:HashTags(int)
    local str = ""
    for i = 1, int do
        str = str.."#"
    end
    return str
end

function LordsFilter:Filter(text)
    if not text then text = "" end
    local tbl = string.Explode(" ", text)
    for k, v in ipairs(tbl) do
        if LordsFilter.BadWords[string.lower(v)] then
            text = string.Replace(text, v, LordsFilter:HashTags(#v))
        end
    end
    
    return text
end